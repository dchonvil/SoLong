#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		c;
	int		size;
	char	*box;

	c = 0;
	size = n;
	box = s;
	while (c < size)
	{
		box[c] = '\0';
		c++;
	}
}
