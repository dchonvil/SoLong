#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t			c;
	unsigned long	i;

	c = 0;
	i = 0;
	while (src[c] != '\0')
		c++;
	if (size == 0)
		return (c);
	while (i < (size - 1) && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (c);
}
